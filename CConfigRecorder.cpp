/*-----------------------------------------------------------------------------
	インクルードファイル
-----------------------------------------------------------------------------*/
#include "CConfigRecorder.h"

/*-----------------------------------------------------------------------------
	静的メンバ変数の初期化
-----------------------------------------------------------------------------*/
int CConfigRecorder::m_config[CI_MAX];

/*-----------------------------------------------------------------------------
	生成
-----------------------------------------------------------------------------*/
CConfigRecorder* CConfigRecorder::Create(void)
{
	CConfigRecorder* pObj;
	
	pObj = new CConfigRecorder();
	pObj->Init();
	
	return pObj;
}

/*-----------------------------------------------------------------------------
	初期化
-----------------------------------------------------------------------------*/
void CConfigRecorder::Init(void)
{
	for(int i = 0; i < CI_MAX; i++)
	{
		m_config[i] = 0;
	}
}

/*-----------------------------------------------------------------------------
	設定
	param	config_id	どのステージIDかの値
	param	val			設定値
-----------------------------------------------------------------------------*/		
void CConfigRecorder::Set(CONFIG_ID config_id, int val)
{
	if(config_id < 0 || config_id >= CI_MAX)
	{
		return;
	}
	
	m_config[config_id] = val;
}

/*-----------------------------------------------------------------------------
	取得
	return	int			設定している値（-1ならばIDの値がおかしい）
	param	config_id	どのステージIDかの値
-----------------------------------------------------------------------------*/			
int CConfigRecorder::Get(CONFIG_ID config_id)
{
	if(config_id < 0 || config_id >= CI_MAX)
	{
		return -1;
	}
	
	return m_config[config_id];
}
