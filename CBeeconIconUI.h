#include "CScene.h"

class CScene2D;

//*****************************************************************************
// ビーコンアイコンクラス
//*****************************************************************************
class CBeeconIconUI : public CScene
{
public:

	// アイコンタイプ
	enum ICONTYPE
	{
		ICONTYPE_METAL = 0,
		ICONTYPE_MINIMUM,
		ICONTYPE_POWERFUL,
		ICONTYPE_NONE,
		ICONTYPE_MAX,
	};

	//=============================================================================
	// コンストラクタ
	//=============================================================================
	CBeeconIconUI();

	//=============================================================================
	// デストラクタ
	//=============================================================================
	~CBeeconIconUI();

	//=============================================================================
	// 初期化処理
	//=============================================================================
	HRESULT Init(void);

	//=============================================================================
	// 終了処理
	//=============================================================================
	void Uninit(void);

	//=============================================================================
	// 更新処理
	//=============================================================================
	void Update(void);

	//=============================================================================
	// 描画処理
	//=============================================================================
	void Draw(void);

	//=============================================================================
	// 生成処理
	// arg : D3DXVECTOR3 pos(生成座標)
	//=============================================================================
	static CBeeconIconUI* Create(D3DXVECTOR3 pos);

	//=============================================================================
	// アイコンタイプセッター
	//=============================================================================
	void SetIconType(ICONTYPE icontype){ m_iconType = icontype; ChangeTexture(); }

	//=============================================================================
	// ポジションゲッター
	//=============================================================================
	D3DXVECTOR3 GetPosition(void){ return m_pos; }
	D3DXVECTOR3 GetRotation(void){ return m_rot; }

private:
	//=============================================================================
	// テクスチャ切替処理
	//=============================================================================
	void ChangeTexture(void);

	void SetPosition(D3DXVECTOR3 pos){ m_pos = pos; }
	void SetPosition(float x, float y, float z){ m_pos = D3DXVECTOR3(x, y, z); }
	void SetRotation(D3DXVECTOR3 rot){ m_rot = rot; }
	void SetRotation(float x, float y, float z){ m_rot = D3DXVECTOR3(x, y, z); }

	static const int TEXTURE_OFFSET = 3;

	ICONTYPE m_iconType;										// アイコンタイプ
	D3DXVECTOR3 m_pos;
	D3DXVECTOR3 m_rot;
	CScene2D *m_pBeeconIcon;
};

